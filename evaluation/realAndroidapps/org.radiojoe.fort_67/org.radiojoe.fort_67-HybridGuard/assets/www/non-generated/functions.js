function restartGps()
{
	if (watchID) {
		navigator.geolocation.clearWatch(watchID);
	}
	var options = { frequency: 1000, enableHighAccuracy: true, maximumAge: 3000 };
	watchID = navigator.geolocation.watchPosition(onGeolocationSuccess, onGeolocationError, options); 	
}

