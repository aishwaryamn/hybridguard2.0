// Called when capture operation is finished
//
function captureSuccess(mediaFiles) {
	var i, len;
	for (i = 0, len = mediaFiles.length; i < len; i += 1) {
		uploadFile(mediaFiles[i]);
	}
	kanaka_next();      
}

// Called if something bad happens.
// 
function captureError(error) {
	var msg = 'An error occurred during capture: ' + error.code;
	navigator.notification.alert(msg, null, 'Uh oh!');
	kanaka_next();
}

// A button will call this function
//
function captureImage() {
	// Launch device camera application, 
	// allowing user to capture up to 1 image
	navigator.device.capture.captureImage(captureSuccess, captureError, {limit: 1});
}

function captureVideo() {
	// Launch video application
	navigator.device.capture.captureVideo(captureSuccess, captureError, {limit: 1});
}

// Upload files to server
function uploadFile(mediaFile) {
	var ft = new FileTransfer(),
		path = mediaFile.fullPath,
		name = mediaFile.name;

	ft.upload(path,
		"http://joeo.montanab.com/photo_upload.php",
		function(result) {
			console.log('Upload success: ' + result.responseCode);
			console.log(result.bytesSent + ' bytes sent');
		},
		function(error) {
			console.log('Error uploading file ' + path + ': ' + error.code);
		},
		{ fileName: name });   
}

function apple_step(position)
{
	if (typeof GlobalObj.Apple.panel != 'undefined') {
		// p1 is the current position
		var p1 = new LatLon(Geo.parseDMS(position.coords.latitude), Geo.parseDMS(position.coords.longitude));
		
		// Loop through all locations...
		for (var name in GlobalObj.locations["Apple"]) {
			var loc = GlobalObj.locations["Apple"][name];
	   
			// If we haven't already been here and this is the step we're on ...
			if (! loc.seen && (loc.step == GlobalObj.Apple.step)) {
				var p2 = new LatLon(Geo.parseDMS(loc.lat), Geo.parseDMS(loc.lon));

				// And we're within X meters ... 
                                console.log('Distance:');
                                console.log( (p1.distanceTo(p2) * 1000) );
				if ((p1.distanceTo(p2) * 1000) < 15 || ((p1.distanceTo(p2) * 1000) < 30 && name == 'Apple1')) {
					
					// And the user isn't busy ...
					if (! GlobalObj.locked) {
						
                                                navigator.notification.vibrate(1000);
						Ext.Msg.alert('', '<img src="images/found_something_icon.png" />');
						// Then move them forward ...
						apple_next();						
						restartGps();
					}
					
					// Remember that we've been here ...	
					GlobalObj.locations["Apple"][name].seen = true;
				
				}
			}
		   
		}
	}
}

