function update_homescreen(position)
{
    var p1 = new LatLon(Geo.parseDMS(position.coords.latitude), Geo.parseDMS(position.coords.longitude));
    var p2 = new LatLon(Geo.parseDMS(GlobalObj.locations["Kanaka"]["Kanaka1"].lat), Geo.parseDMS(GlobalObj.locations["Kanaka"]["Kanaka1"].lon));

    var element = document.getElementById('welcome_screen');
    // If we're within a mile of the first coord of the kanaka module
    if ((p1.distanceTo(p2) * 1000) < 1610 && (p1.distanceTo(p2) * 1000) > 1) {
        if (element.src.search("slide1.jpg") >= 0 || element.src.search('closed_doors.jpg') >= 0) {
			
			var step = 0;
			/*
			 *  Slide 1 gets 0.5 seconds;
				Slides 2-11 get 0 seconds (no delay)
				Slide 12 gets 1 second
				Slides 13-20 get 0 seconds (no delay)
				Slide 21-39 get 0.1 seconds each (to slow down the footsteps just a bit).
				Slides 40-43, 0 seconds
			*/
			function frame() {
				step++;
				if (step < 42) {
					var newSrc = 'images/onsite_animation/slide'+step+'.jpg';
					$("#welcome_screen").attr("src", newSrc);
					var delay = 0;
					if (step == 1) {
						delay = 500;
					}
					else if (step >= 2 && step <= 11) {
						delay = 35;
					}
					else if (step == 12) {
						delay = 3500;
					}
					else if (step >= 13 && step <= 20) {
						delay = 50;
					}
					else if (step >= 21 && step <= 39) {
						delay = 100;
					}
					else {
						delay = 50;
					}
					setTimeout(frame, delay);
				}
				else {
					$("#homepanel").html('<div style="position: absolute; top: 40%; left: 31%; text-align:center;"><b>Pick your path</b></div><img src="images/onsite_animation/slide42.jpg" id="welcome_screen" name="welcome_screen" width="100%" />');
				}
			}
			
			frame();
			
        }
    }
    else {
        if (element.src.search('slide42.jpg') >= 0 || element.src.search('slide1.jpg') >= 0) {
            //element.src = 'images/closed_doors.jpg';
            			
			var step = 0;

			function offsite_frame() {
				step++;
				if (step < 7) {
					var newSrc = 'images/offsite_animation/slide'+step+'.jpg';
					$("#welcome_screen").attr("src", newSrc);
					var delay = 40;
					
					setTimeout(offsite_frame, delay);
				}
				else {
					$("#homepanel").html('<div style="position: absolute; top: 32%; text-align:center; padding: 45px;">For the full app<br /> features, please visit<br><b>The Fort Vancouver National Historic Site</b><br>612 E. Reserve St.<br>Vancouver, Wash., 98661<br><a href="tel:360-816-6230">360-816-6230</a><br><b><a href="http://www.nps.gov/fova" target="_blank">www.nps.gov/fova</a></div><img src="images/offsite_animation/slide6.jpg" id="welcome_screen" name="welcome_screen" width="100%" />');
				}
			}
			
			offsite_frame();
			
        }
    }
   
}

